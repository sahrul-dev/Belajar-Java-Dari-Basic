// Perent Class
public class Film {
  // access modifer variable
  private String judul;
  private String negara;
  private int tahun;

// Getter and Setter
  public void setJudul(String judul){
    this.judul = judul;
  }
  public String getJudul(){
    return judul;
  }
  public void setNegara(String negara){
    this.negara = negara;
  }
  public String getNegara(){
    return negara;
  }
  public void setTahun(int tahun){
    this.tahun = tahun;
  }
  public int getTahun(){
    return tahun;
  }

}
